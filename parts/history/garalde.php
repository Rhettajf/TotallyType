  <p>Garalde or Old Style types start to show the steadily improving skills of punchcutters, characterised by greater contrast between thick and thin strokes, and are generally sharper and more refined. You can see this in the serifs: in Garalde types the serifs on the ascenders are more wedge shaped.</p>
