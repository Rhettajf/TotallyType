        <h5>1700's</h5>
        <p>We may look at the printing press as a revolutionary invention, however, in the first half century of printing, many felt that printed books were inferior to hand-lettered books. The print industry met resistance from the scribal trade, people feared loosing their jobs and in return were slow to accept the emerging industry. Printers began to show how profitable printed books could be and consumers saw increased affordability and availability of books. The Catholic Church saw printed books as a way to help spread Christianity and endorsed printers. Before long, collecting books became part of culture.</p>
        <p>With a wide acceptance of printing, publishers no longer felt the need to make their books appear to have been hand-lettered. Garalde types marked a subtle shift away from a calligraphic origin. Additionally, more sophisticated molding technology allowed for greater contrast between thick and thin strokes and more delicate serifs.</p>
        <h5>1744</h5>
        <p>The Discovery of Chlorine. The Swedish chemist Carl Wilhelm Scheele discovers Chlorine. Chlorine was used in the bleaching of paper producing the clean white look we have today.</p> 
