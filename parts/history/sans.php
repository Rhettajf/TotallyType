    <h1 class="grotesque text-center example">Grotesque</h1>
    <h4 class="grotesque text-center hyphens">ABCDEFGHIJKLMNOPQRSTUVWXYZ<br>abcdefghijklmnopqrstuvwxyz</h4>
    <p>These are the first commercially popular sans serif typefaces. Contrast in stroke weight is most apparent in these styles, there is a slight “squared” quality to many of the curves, and many designs have the “bowl and loop” lowercase <i>g</i> common to Roman types. In some cases the <i>R</i> has a curled leg, and the <i>G</i> usually has a spur. This category also includes more modern, sans serif designs patterned after the first grotesques. Stroke contrast is less pronounced than earlier designs, and much of the “squareness” in curved strokes has been rounded. Normally the most obvious distinguishing characteristic of these faces is their single bowl <i>g</i> and more monotone weight stress.</p>
