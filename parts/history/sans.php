    <h1 class="text-sans text-center example">Humanist</h1>
    <h4 class="text-sans text-center hyphens">ABCDEFGHIJKLMNOPQRSTUVWXYZ<br>abcdefghijklmnopqrstuvwxyz</h4>
    <p>These are based on the proportions of Roman inscriptional letters. Frequently, contrast in stroke weight is readily apparent. Typographic experts claim that these are the most legible and most easily read of the sans serif typefaces. Humanistic sans serif typefaces also closely match the design characteristics and proportions of serif types, often with a strong calligraphic influence.</p>
