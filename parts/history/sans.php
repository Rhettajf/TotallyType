     <h5>1786</h5>
    <p>In 1786 a rounded sans-serif font was developed by Valentin Haüy that first appeared in the book titled "Essai sur l'éducation des aveugles" (An Essay on the Education of the Blind). The purpose of this font was accessibility &mdash; designed to emboss paper allowing the blind to read with their fingers. The design was eventually known as Haüy type, the first attempt at brail.</p>
    </div>
    <div class="large-3 columns">
        <h5>1816</h5>
        <p>William Caslon IV created the first typeface without any serifs at all. It was widely rebuked at the time. This was the start of what we now consider Sans Serif typefaces. During this time, type exploded, and many, many variations were being created to accommodate advertising.</p>     
