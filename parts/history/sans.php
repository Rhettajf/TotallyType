    <p>Neo-Grotesque, also called Transitional or Realist are based on the later Grotesques. These are more refined Grotesques with careful construction.
   Type designers were still feeling their way around the new style of the sans serif; thus, the Neo-Grotesques lose many of the awkward curves and idiosyncrasies that are common in Grotesques. Characterized by less variation in line weight and the single-story <i>g</i></p>
