    <h1 class="text-sans example">Sans Serif</h1>
    <h4 class="text-sans text-center hyphens">ABCDEFGHIJKLMNOPQRSTUVWXYZ<br>abcdefghijklmnopqrstuvwxyz</h4>
    <p>By the time slab serifs were in use, sans serifs had already been around for some time. To follow the progression of sans serifs, we must step back in time. The first sans-serif types emerged in the 18th century but did not became popular until the early 19th century. The first sans serifs are classified as grotesque as loosing the serif was a departure from hundreds of years of tradition.</p>
    
	<p>As always, simplicity requires the greatest effort. With fewer decorative distractions, sans-serif typefaces need to be flawless in proportion, color, and balance. The major difference besides the lack of serifs is contrast. Sans serif letters have low contrast, usually 75&ndash;90%. Many feel that sans serifs have less personality then serif types. This is far from the truth. Sans serifs are simpler but less tied to historical contexts, making them great for abstraction and creative exploration. There are four basic classifications of sans-serif typefaces: Grotesque, Neo-grotesque, Geometric, and Humanist.</p>
