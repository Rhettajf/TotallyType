      <h1 class="venetian example">Venetian</h1>
      <h4 class="venetian text-center hyphens">ABCDEFGHIJKLMNOPQRSTUVWXYZ<br>abcdefghijklmnopqrstuvwxyz</h4>
    <p>Movable type spread across Europe, and in less than fifty years, there were thousands of printers. As more people began to read, words became shapes and the demand for legibility and aesthetic appeal became greater. Movable type opened up opportunities for reproducing texts creating a demand for typefaces.</p> 
      <p>The hand-inspired Pre-Venetian lettering was difficult to read and time consuming to set; it did not reflect the demands and productivity of Gutenberg's printing press. In response, typesetters began looking for inspiration in various writing forms. Venetian, also called Humanist and Renaissance Antiqua, typefaces have the clearest relationship to pen-formed writing; the oblique axis is severe, the contrast is low and the letter components share the abrupt modeling of the broad-tip pen.</p>
