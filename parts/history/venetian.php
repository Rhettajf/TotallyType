        <h4 class="push-top">Italy 1462</h4>
        <p>Mainz, the German printing capital, is sacked. Printers are forced to fled &mdash; most go to Italy where they are exposed to the Renaissance movement that was drawing Europe away from medieval practices. </p>
        <p>Rather than using  blackletter or Fraktur type, printers began to create type that mimicked the hand of Italian humanist writers.</p>
