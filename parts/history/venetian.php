<div class="row">
    <div class="large-3 columns">
        <h4 class="pull-bottom">c.1500 Aldus Manutius</h4>
        <img src="img/history/Manutius_oc.jpg" alt=""/>
        <p>As print spread so did the demand for books. Early books were very large. To meet the need of scholars and students, Aldus Manutius wanted to produce books that could be transported. He invented the 'pocket edition' called an octavos that could be carried and read anywhere. Pocket books were the first version of the modern paperback and some even featured only text.</p> 
	</div>
    <div class="large-3 columns">
        <h5>1501 Italics</h5>    
        <img src="img/history/Manutius_italic.jpg" alt=""/>
        <p>To fit more words on a page, Aldus Manutius commissioned Griffo to cut a condensed typeface. Griffo based it upon chancery manuscript, a contemporary cursive roman face. It became known as Aldinian. Today, we call it italics and use it for <a href="family.php#emphasis" title="Emphasis" target="_blank">emphasis</a>.</p>
  
