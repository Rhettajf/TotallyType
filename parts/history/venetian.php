        <h4>Nicolas Jenson 1470</h4>
        <p>Inspired by the text on ancient roman buildings, Nicolas Jenson combined a Gothic calligraphic style with Italian humanist handwriting, creating the first Roman Type. Jenson's type  was highly legible and evenly colored, it was much more readable than blackletter, and caught on quickly.</p>
