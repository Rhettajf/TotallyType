        <p>The Didone period partially overlaps the Transitional period. Didones reflect expressive ideals of Romanticism. The Didone style exaggerates key features of Transitionals. Letters are drawn with vertical axis, uniform widths, thin straight serifs, and a sharp contrast in the strokes.</p>
