        <h5>Late 1700's</h5>
        <p>Fine hairline strokes and serifs did not lend themselves to reversal or stippling except in large sizes. Such fine details print best on a smooth, matt-finish, white paper in black ink. Improvements in paper production, composition, printing and binding during the late eighteenth century made it possible to develop a type style with strong vertical emphasis and fine hairlines.</p>
    </div>
    <div class="large-3 columns">
        <h5>1780</h5>
        <p>This is what the French family Didot did, and what the Italian printer Giambattista Bodoni perfected. Firmin Didot and Giambattista Bodoni created the first ‘modern’ Roman typefaces Didone (Didot, and Bodoni). The contrasts were more extreme than ever before.</p> 
