        <p>Typically, anatomy is taught referencing a serif typeface. However, both sans and serif typefaces share a global anatomy. The basic structure of letters can be used to classify and compare the appearance of different typefaces. By learning what every typeface has in common first (the core parts of letters) it is easier to spot the differences that make classification and identification possible. Additionally, anatomy can be grouped into three sections, strokes, counter-space, and accessories which are variable identifiers.</p>
