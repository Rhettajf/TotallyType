<div class="row">
    <div class="large-3 columns">
        <h4>Ascender Height</h4>
        <p>Also called the topline, the ascender height is where the top of letters such as <i>k</i> and <i>h</i> reach above the cap height.</p>
    </div>
    <div class="large-3 columns">
        <h4>Cap Height</h4>
        <p>The height of capital letters, cap height is the distance from the baseline to the top of capital letters.</p>
    </div>
</div>
<div class="row">
    <div class="large-3 columns">
        <h4>x-height</h4>
        <p>The x-height is the distance between the baseline and the top of lower case letters. Specifically the height of the lowercase <i>x</i>. The x-height is a factor in typeface identification and readability.</p>
    </div>
    <div class="large-3 columns">
        <h4>Baseline</h4>
        <p>The baseline is the imaginary line upon which a line of text rests. The baseline is critical for aligning text. In most typefaces round letters such as <i>a</i> or <i>o</i> overshoot the baseline.</p>
    </div>
</div>
<div class="row">
    <div class="large-3 columns">
        <h4>Descender Depth</h4>
        <p>Descender depth is how far descenders (such as in the letters <i>p</i> and <i>y</i>) hang or descend below the baseline.</p>
    </div>
