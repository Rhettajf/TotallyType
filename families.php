<p class="push-top no-indent">Back in the days of movable type, printers needed thousands of metal blocks, each a single character. If you wanted to set your text in Garamond, you needed different blocks for every character, point size and weight you wanted to use. This is where we get the terms <i>typeface</i> and <i>font</i>. Those thousands of metal blocks of Garamond was a typeface, a single typeface design. You couldn't call every variation, all those different blocks, Garamond so a subset of blocks, a particular size, weight, or style, was called a font. Garamond 10 point was a different font than Garamond 8 point. The two terms became somewhat synonymous with the rise of desktop publishing and digital fonts. Fonts were no longer thousands of blocks â€” but thousands of bytes that scaled themselves up or down to any size &mdash; see <a href="units.php" title="units" target="_self">units</a> to learn more. A <b>typeface</b> is a family of fonts.</p>

<h2>Family</h2>
<p>Printers began organizing roman and italic fonts into families as early as the sixteenth century. However, it wasn't until the the beginning of the twentieth century that the family as we know it was formalized. Today, a family include styles such as italic, bold, and small caps.</p>
