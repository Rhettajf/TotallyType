<h1>Punctuation</h1>

<h3>Hyphens &amp; Dashes</h3>

<p>The hyphen is the punctuation mark that automatically appears at the end of a line when a word breaks onto the next in desktop publishing software, this is called hypenation. See alignment to learn more about using hyphenation. Additionally, the hyphen is used to link two or more words — such as line-height or pixel-per-inch.</p>


<p>The em dash is used to break or pause between parts of a sentence when you want more emphasis than a comma but less than a colon, semicolon, or parenthesis. The en dash is used to indicate a range of values (1880–1912, 330–39, A–E). It is also used to denote a connection or contrast two words (Hartford–Savannah). Both em and en dashes are set flush against letterforms; if they look too cramped it is OK in print and favorable on screen to add a space or better yet a thin space before and after the dash.</p>

<ul class="single-space-list">
<li>The em dash is approximately 1 em wide.</li>
<li>The en dash is approximately 0.5 em wide.</li>
</ul>

